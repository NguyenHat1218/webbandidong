                        <B>2. Năng lượng sạch</B><br />
                        Những nổ lực chống lại biến đổi khí hậu đang dần dần thay đổi cách chúng ta sử dụng năng lượng.
                        May mắn là các nhà khoa học, kỹ sư và doanh nhân cũng nhận ra điều này và đang biến những nguồn
                        năng lượng sạch tiện dụng hơn, chi phí hấp dẫn hơn.
                        Nhờ những tiến bộ trong công nghệ và trong sản xuất, giá pin mặt trời giảm rất nhiều, đến
                        99,5% so với năm 1977. Năng lượng mặt trời sẽ sớm cạnh tranh về chi phí so với năng lượng hóa
                        thạch, Ngoài ra, năng lượng gió cũng giảm liên tục.<br />
                        <img src="images/nangluongsach.jpg" style="width:750px;height:300px"> <br />
                        Những tổ chức ủng hộ năng lượng sạch cũng đang tận dụng xu hướng này. Ví dụ, Ấn độ có sáng kiến
                        chuyển các sân bay thành những nông trại năng lượng sạch, tự cung cấp nguồn năng lượn. Tesla đưa
                        ra xe ô tô điện vừa nhanh, vừa mạnh và sạc điện qua các trạm sạc đang được lắp đặt trên toàn
                        cầu.

                        Ngoài ra, cũng có những dấu hiệu tươi sáng cho thấy năng lượng sạch đang đạt đến điểm bùng phát.
                        Ví dụ, tại Nhật Bản, hiện có rất nhiều trạm sạc điện hơn cây xăng. Còn tại Đức, vào vài thời
                        điểm, năng lượng sạch được sản xuất nhiều hơn nhu cầu sử dụng của người dân.<br />
                        <B> 3. Thực tế ảo và Tương tác ảo:</B><br />

                        Bộ xử lý máy tính mới chỉ đủ mạnh để giúp người dùng trải nghiệm thử môi trường Thực tế ảo và
                        Tương tác ảo. Nhưng công ty như Facebook, Google, Apple và Microsoft đang đầu tư hàng tỉ USD để
                        biến thực tế ảo (VR) và tương tác ảo (AR) trở nên phổ biến hơn, thuận tiện hơn và dễ dùng hơn.
                        Một số người hiện còn nghĩ rằng VR và AR mới chỉ áp dụng cho Game, nhưng dần dần, công nghệ này
                        áp dụng cho mọi hoạt động đời sống. Ví dụ, chúng ta sử dụng chúng để xây dựng các mô hình, vật
                        thể 3D, gặp gỡ bạn bè, đối tác khắp thế giới, hay điều trị bệnh...VR và AR xuất hiện từ lâu
                        trong các tiểu thuyết hay phim khoa học viễn tưởng, nhưng trong vài năm tới, chúng sẽ trở nên
                        phổ biến.<br />
