                        <B>4. Thiết bị bay không người lái và Xe bay:</B><br />

                        Trước đây, GPS chỉ được dùng trong quân đội, nhưng nay công nghệ này được sử dụng rộng rãi trong
                        Taxi, chỉ đường và săn Pokemon. Tương tự, thiết bị bay không người lái cũng khởi nguồn phục vụ
                        cho quân đội nhưng cầng ngày càng được sử dụng rộng rãi trong thị trường tiêu dùng và doanh
                        nghiệp. Ví dụ, Drone hiện được dùng để khảo sát các kiến trúc quan trọng như cầu cống, đường dây
                        điện và khảo sát các khu vực bị thiên tai, lũ lụt gây ra. Drone cũng được ứng dụng trong nhiều
                        trường hợp khác.<br />
                        <img src="images/thietbibay.jpg" style="width:750px;height:300px"> <br />
                        Amazon và Google đang chế tạo Drone để giao nhận hàng hóa. Công ty khởi nghiệp Zipline dùng
                        Drone để giao thuốc chữa bệnh đến các vùng sâu, vùng xa không có đường xá thuận tiện.
                        Cũng đã có vài công ty khởi nghiệp nghiên cứu xe bay, trong đó có hai công ty do đồng sáng lập
                        Google, Larry Page đầu tư xe bay dùng công nghệ như trong Drone nhưng có thể chở người. Dựa vào
                        những tiến bộ trong chất liệu, Pin và phần mềm, xe bay sẽ là phương tiện di chuyển rất tiện lợi
                        so với máy bay và trực thăng ngày nay.<br />
                        <B>5. Trí tuệ nhân tạo </B><br />

                        Năm 1997, tờ New York Times cho rằng phải mất một trăm năm sau máy tính mới có thể thắng được
                        người trong môn cờ vây. Nhưng ngay trong năm này, tờ New York Times lại nói kiện tướng cờ vây đã
                        bị chương trình máy tính của Google đánh bại.Trí thông minh nhân tạo đã tiến bộ vượt bậc trong
                        thập kỷ vừa qua nhờ vào những thuật toán mới, sức mạnh tính toán và dữ liệu lớn. AI có thể áp
                        dụng cho gần như mọi lĩnh vực.

                        Google tạo một hệ thống AI điều khiển các trung tâm dữ liệu quản lý về điện năng của họ, tiết
                        kiệm được hàng trăm triệu USD chi phí năng lượng.

                        Tiềm năng dễ thấy nhất của AI là giải phóng con người thoát khỏi những công việc đàu óc mang
                        tính lặp đi lặp lại, như cách cuộc cách mạng công nghiệp giải phóng con người ra khỏi những công
                        việc tay chân.
                        <img src="images/ai.jpg" style="width:750px;height:300px"> <br />
                        Một số người lo ngại AI sẽ khiến nhiều người thất nghiệp. Lịch sử đã chứng minh rằng khi công
                        nghệ mới xuất hiện, nó loại đi những công việc cũ nhưng lại tạo ra nhiều công việc mới tốt hơn.
                        Ví dụ khi máy tính cá nhân xuất hiện, nó giết đi công việc về đánh máy chữ nhưng lại tạo ra các
                        công việc về thiết kế, chế bản điện tử tốt hơn.

                        Chúng ta dễ dàng xác định công việc bị loại bỏ hơn so với công việc mới phát sinh. Ngày nay,
                        hàng triệu người làm việc như là nhà phát triển ứng dụng, tài xế lái xe cho các dịnh vụ chia sẻ
                        xe, các nhân viên điều khiển drone, nhà tiếp thị trên mạng xã hội... Trong chục năm tới, rất khó
                        để xác định được sẽ xuất hiện những nghề nghiệp nào mới.


