                 <CENTER><h1 style="color:DodgerBlue;">LỊCH SỬ HÌNH THÀNH CỦA SHOP</h1></CENTER>
       
        <p>Điện thoại thông minh hay smartphone (tiếng Anh: smartphone) là khái niệm để chỉ loại điện thoại di động
            thích hợp một nền tảng hệ điều hành di động với nhiều tính năng hỗ trợ tiên tiến về điện toán và có khả năng
            kết nối với nhiều thiết bị điện tử hiện đại như TV thông minh, máy tính, robot, nhà thông minh, tích hợp
            hoặc không trí thông minh nhân tạo, dựa trên nền tảng cơ bản của điện thoại di động thông thường (điện thoại
            phổ thông) (tiếng Anh: feature phone).<br/>
            <img src="images/ai.jpg" style="width:750px;height:300px"><br/>

            Khái niệm smartphone ra mắt từ những năm 2003–2005. Ban đầu điện thoại thông minh bao gồm các tính năng của
            điện thoại di động thông thường kết hợp với các thiết bị phổ biến khác như PDA, thiết bị điện tử cầm tay,
            máy ảnh kỹ thuật số, hệ thống định vị toàn cầu GPS. Điện thoại thông minh hiện đại ngày nay bao gồm hầu như
            tất cả chức năng của laptop, máy tính như duyệt web, Wi-Fi, đồ họa, văn phòng, chơi game, chụp ảnh, quay
            phim, video call, định vị toàn cầu, trợ lý ảo, các ứng dụng của bên thứ 3 trên Kho ứng dụng di động và các
            phụ kiện đi kèm cho máy. Thậm chí một số smartphone cao cấp còn đóng vai trò như một món đồ trang sức đắt
            tiền, tô điểm cho người chủ nhân.

            Trong lịch sử đã từng có nhiều nền tảng hệ điều hành di động cũng như nhiều phong cách thiết kế được sinh ra
            và bị khai tử. Năm 2007, với sự ra đời của chiếc iPhone thế hệ đầu tiên của Apple với màn hình cảm ứng điện
            dung, iPhone đã được coi là sự định hình cho kiểu dáng thiết kế điện thoại thông minh hiện đại.<br/>
            <img src="images/chinhanh1.jpg" style="width:750px;height:300px"><br/>


            Chiếc iPhone đầu tiên ra mắt năm 2007 - chiếc điện thoại định hình thế giới smartphone hiện đại
            Những điện thoại thông minh phổ biến nhất hiện nay dựa trên nền tảng của 2 hệ điều hành trụ lại thành công
            duy nhất là Android của Google và iOS của Apple
            Định nghĩa công nghiệp về smartphone là một thiết bị điện thoại thông minh có một màn hình cảm ứng với kích
            thước và độ phân giải cao hơn so với điện thoại truyền thống. Điện thoại thông minh được coi như một máy
            tính di động kết hợp với máy ảnh kỹ thuật số và thiết bị chơi game cầm tay, vì nó có một hệ điều hành riêng
            biệt được thiết kế để hiển thị phù hợp các trang website một cách bình thường cùng nhiều chức năng khác của
            máy tính như thiết kế, đồ họa, chơi game, cũng như chụp ảnh và quay phim.<br/>
            <img src="images/chinhanh2.jpg" style="width:750px;height:300px"><br/>

            Người dùng có thể thay đổi một giao diện (User interface) trên smartphone của mình (hình nền, cách bố trí
            các ứng dụng), cũng như sở hữu khả năng cài đặt thêm hoặc gỡ bỏ một ứng dụng nào đó được cung cấp trên Kho
            ứng dụng di động.

            Với việc tích hợp những bộ vi xử lý, những con chip di động (SoC) ngày càng mạnh mẽ, điện thoại thông minh
            có thể tiến hành đa tác vụ đa thao tác, xử lý đa phương tiện ngày càng mạnh mẽ, kích thích sự phát triển của
            công nghệ viễn thông và khoa học công nghệ. Kết nối dễ dàng với Internet, xử lý Email trong giây lát và chơi
            những tựa game nặng nhất. Smartphone dần thay thế các thiết bị giải trí truyền thống như: máy nghe nhạc cầm
            tay MP3, PDA, máy ảnh kỹ thuật số, máy ghi âm, máy quay phim cầm tay, máy chơi game, TV, máy đọc sách,...
            với chất lượng ngày càng cao.<br/>
            <img src="images/chinhanh3.jpg" style="width:750px;height:300px"><br/>

            Điện thoại thông minh có thể thay thế xử lý các vấn đề máy tính văn phòng và các vấn đề khác, nó có thể giao
            tiếp với mạng duy trì một thời gian kết nối liền mạch, đồng thời có thể vô hiệu hóa mạng bất cứ lúc nào, và
            đồng bộ hóa dữ liệu với máy tính, máy tính xách tay, robot, ra lệnh bằng giọng nói với trí thông minh nhân
            tạo, điều khiển nhà thông minh và các thiết bị điện tử khác.<br/>
            <img src="images/toanha.jpg" style="width:750px;height:300px">
